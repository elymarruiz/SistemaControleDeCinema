    private void labelLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_labelLoginMouseClicked
        if(labelLogin.getText().equals(Constantes.LABEL_LOGIN)){
            if (Formularios.telaLogin == null)
            Formularios.telaLogin = new TelaLogin();

            Formularios.telaLogin.setModal(true);
            Formularios.telaLogin.setVisible(true);
        }else{
            int escolha = 
                JOptionPane.showConfirmDialog(
                        null, 
                        Constantes.PERGUNTA_ENCERRAR_SESSAO);
        
            if(escolha == JOptionPane.YES_OPTION){
                DadosTemporarios.idUsuarioLogado = -1;
                DadosTemporarios.usuarioLogado = null;
                labelLogin.setText(Constantes.LABEL_LOGIN);
                
                ((DefaultTableModel) tableCompra.getModel()).setNumRows(0);
            }
        }    
    }//GEN-LAST:event_labelLoginMouseClicked

